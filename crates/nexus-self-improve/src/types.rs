// ── System State (input to the pipeline) ────────────────────────────

/// Rolling metric baseline for anomaly detection.
#[derive(Debug, Clone, Serialize, Deserialize)]
pub struct MetricBaseline {
    pub mean: f64,
    pub std_dev: f64,
    pub sample_count: u64,
}

/// Snapshot of system metrics for the Observer.
#[derive(Debug, Clone, Default)]
pub struct SystemMetrics {
    pub metrics: Vec<(String, f64)>,
}

impl SystemMetrics {
    pub fn new() -> Self {
        Self {
            metrics: Vec::new(),
        }
    }

    pub fn insert(&mut self, name: impl Into<String>, value: f64) {
        self.metrics.push((name.into(), value));
    }

    pub fn iter(&self) -> impl Iterator<Item = (&str, &f64)> {
        self.metrics.iter().map(|(k, v)| (k.as_str(), v))
    }
}

/// Context available to the Proposer for generating changes.
#[derive(Debug, Clone, Default)]
pub struct SystemContext {
    pub agent_configs: serde_json::Value,
    pub current_policies: serde_json::Value,
    pub recent_audit_summary: String,
}

/// Full system state passed into the pipeline.
#[derive(Debug, Clone)]
pub struct SystemState {
    pub metrics: SystemMetrics,
    pub context: SystemContext,
    pub audit_chain_valid: bool,
    pub test_suite_passing: bool,
}

// ── Cycle Result ────────────────────────────────────────────────────

/// Result of a single pipeline cycle.
#[derive(Debug)]
pub enum CycleResult {
    /// No signals detected — system is healthy.
    NoSignals,
    /// Signals detected but no actionable opportunities.
    NoOpportunities,
    /// Proposal generation failed.
    ProposalFailed(String),
    /// Validation failed (invariant violation, test failure, HITL denial).
    ValidationFailed(String),
    /// Application failed (checkpoint or test failure post-apply).
    ApplyFailed(String),
    /// Improvement successfully applied and entering canary period.
    Applied(AppliedImprovement),
}
