//! Data provenance and lineage tracking.
//!
//! Provides structured tracking of data origin, transformations, and flow
//! through agents. Hooks into existing audit events to automatically build
//! lineage records.

use crate::audit::{AuditTrail, EventType};
use serde::{Deserialize, Serialize};
use serde_json::json;
use std::collections::HashMap;
use uuid::Uuid;

// ---------------------------------------------------------------------------
// Core types
// ---------------------------------------------------------------------------

/// Where a piece of data originated.
#[derive(Debug, Clone, PartialEq, Eq, Hash, Serialize, Deserialize)]
pub enum DataOrigin {
    /// Provided directly by a user.
    UserInput,
    /// Generated by an LLM.
    LlmResponse,
    /// Read from the filesystem.
    FileRead,
    /// Received from another agent.
    AgentMessage,
    /// Fetched from an external API or service.
    ExternalApi,
}

impl DataOrigin {
    pub fn as_str(&self) -> &'static str {
        match self {
            Self::UserInput => "user_input",
            Self::LlmResponse => "llm_response",
            Self::FileRead => "file_read",
            Self::AgentMessage => "agent_message",
            Self::ExternalApi => "external_api",
        }
    }
}

/// A transformation applied to data during processing.
#[derive(Debug, Clone, PartialEq, Eq, Serialize, Deserialize)]
pub struct Transformation {
    /// What happened to the data.
    pub kind: TransformationKind,
    /// Agent that applied the transformation.
    pub agent_id: Uuid,
    /// Unix timestamp when the transformation occurred.
    pub timestamp: u64,
    /// Optional description of what changed.
    pub description: String,
}

/// The type of transformation applied.
#[derive(Debug, Clone, PartialEq, Eq, Hash, Serialize, Deserialize)]
pub enum TransformationKind {
    /// PII or sensitive data was redacted.
    Redacted,
    /// Data was summarised or condensed.
    Summarized,
    /// Data was classified or categorised.
    Classified,
    /// Data was delegated to another agent.
    DelegatedToAgent,
    /// Data was sent to an LLM for processing.
    SentToLlm,
    /// Data was written to a file or output.
    WrittenToOutput,
    /// Custom transformation.
    Custom(String),
}

impl TransformationKind {
    pub fn as_str(&self) -> &str {
        match self {
            Self::Redacted => "redacted",
            Self::Summarized => "summarized",
            Self::Classified => "classified",
            Self::DelegatedToAgent => "delegated_to_agent",
            Self::SentToLlm => "sent_to_llm",
            Self::WrittenToOutput => "written_to_output",
            Self::Custom(s) => s,
        }
    }
}

/// Sensitivity classification for tracked data.
#[derive(Debug, Clone, Copy, PartialEq, Eq, Hash, Serialize, Deserialize)]
pub enum DataClassification {
    Public,
    Internal,
    Confidential,
    Restricted,
}

impl DataClassification {
    pub fn as_str(self) -> &'static str {
        match self {
            Self::Public => "public",
            Self::Internal => "internal",
            Self::Confidential => "confidential",
            Self::Restricted => "restricted",
        }
    }
}

/// A single lineage record tracking one piece of data through the system.
#[derive(Debug, Clone, PartialEq, Eq, Serialize, Deserialize)]
pub struct DataLineage {
    /// Unique identifier for this data lineage chain.
    pub data_id: Uuid,
    /// Where the data originated.
    pub origin: DataOrigin,
    /// Ordered list of transformations applied to the data.
    pub transformations: Vec<Transformation>,
    /// The agent currently holding the data.
    pub current_holder: Uuid,
    /// Unix timestamp when the lineage record was created.
    pub created_at: u64,
    /// Sensitivity classification.
    pub classification: DataClassification,
    /// Human-readable label for this data item.
    pub label: String,
}

/// Summary of all data an agent has touched.
#[derive(Debug, Clone, PartialEq, Eq, Serialize, Deserialize)]
pub struct AgentLineageReport {
    /// Agent this report is for.
    pub agent_id: Uuid,
    /// All lineage records where this agent is involved.
    pub lineage_entries: Vec<DataLineage>,
    /// Count of data items originated by this agent.
    pub originated: usize,
    /// Count of data items received via delegation.
    pub received: usize,
    /// Count of transformations applied by this agent.
    pub transformations_applied: usize,
    /// Unix timestamp when the report was generated.
    pub generated_at: u64,
}

