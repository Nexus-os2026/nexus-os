/// Tracks data provenance by building lineage records from audit events
/// and explicit registration calls.
#[derive(Debug, Clone, Default)]
pub struct ProvenanceTracker {
    lineage: HashMap<Uuid, DataLineage>,
}

impl ProvenanceTracker {
    pub fn new() -> Self {
        Self::default()
    }

    /// Register a new data lineage entry (e.g. when an agent reads a file).
    pub fn record_origin(
        &mut self,
        origin: DataOrigin,
        agent_id: Uuid,
        classification: DataClassification,
        label: &str,
        audit_trail: &mut AuditTrail,
    ) -> Uuid {
        let data_id = Uuid::new_v4();
        let now = current_timestamp();

        let lineage = DataLineage {
            data_id,
            origin: origin.clone(),
            transformations: Vec::new(),
            current_holder: agent_id,
            created_at: now,
            classification,
            label: label.to_string(),
        };

        self.lineage.insert(data_id, lineage);

        // Audit the origin event
        let _ = audit_trail.append_event(
            agent_id,
            EventType::StateChange,
            json!({
                "event": "provenance.origin",
                "data_id": data_id.to_string(),
                "origin": origin.as_str(),
                "classification": classification.as_str(),
                "label": label,
            }),
        );

        data_id
    }

    /// Record a transformation on tracked data.
    pub fn record_transformation(
        &mut self,
        data_id: Uuid,
        kind: TransformationKind,
        agent_id: Uuid,
        description: &str,
        audit_trail: &mut AuditTrail,
    ) -> bool {
        let now = current_timestamp();

        let Some(lineage) = self.lineage.get_mut(&data_id) else {
            return false;
        };

        let transformation = Transformation {
            kind: kind.clone(),
            agent_id,
            timestamp: now,
            description: description.to_string(),
        };

        lineage.transformations.push(transformation);

        let _ = audit_trail.append_event(
            agent_id,
            EventType::StateChange,
            json!({
                "event": "provenance.transformation",
                "data_id": data_id.to_string(),
                "kind": kind.as_str(),
                "description": description,
            }),
        );

        true
    }

    /// Record a delegation handoff — data moves from one agent to another.
    pub fn record_delegation(
        &mut self,
        data_id: Uuid,
        from_agent: Uuid,
        to_agent: Uuid,
        reason: &str,
        audit_trail: &mut AuditTrail,
    ) -> bool {
        let now = current_timestamp();

        let Some(lineage) = self.lineage.get_mut(&data_id) else {
            return false;
        };

        lineage.transformations.push(Transformation {
            kind: TransformationKind::DelegatedToAgent,
            agent_id: from_agent,
            timestamp: now,
            description: format!("Delegated to agent {} — {}", to_agent, reason),
        });
        lineage.current_holder = to_agent;

        let _ = audit_trail.append_event(
            from_agent,
            EventType::StateChange,
            json!({
                "event": "provenance.delegation",
                "data_id": data_id.to_string(),
                "from_agent": from_agent.to_string(),
                "to_agent": to_agent.to_string(),
                "reason": reason,
            }),
        );

        true
    }

    /// Record that PII redaction was applied to tracked data.
    pub fn record_redaction(
        &mut self,
        data_id: Uuid,
        agent_id: Uuid,
        description: &str,
        audit_trail: &mut AuditTrail,
    ) -> bool {
        self.record_transformation(
            data_id,
            TransformationKind::Redacted,
            agent_id,
            description,
            audit_trail,
        )
    }

    /// Query the full lineage chain for a data item.
    pub fn query_lineage(&self, data_id: &Uuid) -> Option<&DataLineage> {
        self.lineage.get(data_id)
    }

    /// Export a lineage report for an agent — all data the agent has touched.
    pub fn export_lineage_report(&self, agent_id: Uuid) -> AgentLineageReport {
        let now = current_timestamp();
        let mut entries = Vec::new();
        let mut originated = 0usize;
        let mut received = 0usize;
        let mut transformations_applied = 0usize;

        let agent_id_str = agent_id.to_string();

        for lineage in self.lineage.values() {
            // Check if agent ever held or transformed this data
            let agent_involved = lineage.current_holder == agent_id
                || lineage
                    .transformations
                    .iter()
                    .any(|t| t.agent_id == agent_id);

            // Check if data was delegated TO this agent (description contains agent UUID)
            let was_delegated_to = lineage.transformations.iter().any(|t| {
                t.kind == TransformationKind::DelegatedToAgent
                    && t.description.contains(&agent_id_str)
            });

            if !agent_involved && !was_delegated_to {
                continue;
            }

            entries.push(lineage.clone());

            // The agent originated this data if no transformations exist
            // and it's the current holder, or the first transformation is by this agent
            // with no prior delegation
            let is_originator =
                lineage.transformations.is_empty() && lineage.current_holder == agent_id;
            if is_originator {
                originated += 1;
            }

            if was_delegated_to {
                received += 1;
            }

            transformations_applied += lineage
                .transformations
                .iter()
                .filter(|t| t.agent_id == agent_id)
                .count();
        }

        AgentLineageReport {
            agent_id,
            lineage_entries: entries,
            originated,
            received,
            transformations_applied,
            generated_at: now,
        }
    }

    /// Build lineage entries from existing audit trail events.
    ///
    /// Scans the audit trail for provenance-related events and reconstructs
    /// lineage records. Useful when replaying or importing audit history.
    pub fn rebuild_from_audit(&mut self, audit_trail: &AuditTrail) {
        for event in audit_trail.events() {
            let Some(obj) = event.payload.as_object() else {
                continue;
            };
            let Some(event_name) = obj.get("event").and_then(|v| v.as_str()) else {
                continue;
            };

            match event_name {
                "provenance.origin" => {
                    if let (Some(data_id_str), Some(origin_str), Some(label)) = (
                        obj.get("data_id").and_then(|v| v.as_str()),
                        obj.get("origin").and_then(|v| v.as_str()),
                        obj.get("label").and_then(|v| v.as_str()),
                    ) {
                        if let Ok(data_id) = Uuid::parse_str(data_id_str) {
                            let origin = match origin_str {
                                "user_input" => DataOrigin::UserInput,
                                "llm_response" => DataOrigin::LlmResponse,
                                "file_read" => DataOrigin::FileRead,
                                "agent_message" => DataOrigin::AgentMessage,
                                "external_api" => DataOrigin::ExternalApi,
                                _ => continue,
                            };
                            let classification = match obj
                                .get("classification")
                                .and_then(|v| v.as_str())
                                .unwrap_or("internal")
                            {
                                "public" => DataClassification::Public,
                                "confidential" => DataClassification::Confidential,
                                "restricted" => DataClassification::Restricted,
                                _ => DataClassification::Internal,
                            };
                            self.lineage.insert(
                                data_id,
                                DataLineage {
                                    data_id,
                                    origin,
                                    transformations: Vec::new(),
                                    current_holder: event.agent_id,
                                    created_at: event.timestamp,
                                    classification,
                                    label: label.to_string(),
                                },
                            );
                        }
                    }
                }
                "provenance.transformation" => {
                    if let (Some(data_id_str), Some(kind_str)) = (
                        obj.get("data_id").and_then(|v| v.as_str()),
                        obj.get("kind").and_then(|v| v.as_str()),
                    ) {
                        if let Ok(data_id) = Uuid::parse_str(data_id_str) {
                            if let Some(lineage) = self.lineage.get_mut(&data_id) {
                                let kind = match kind_str {
                                    "redacted" => TransformationKind::Redacted,
                                    "summarized" => TransformationKind::Summarized,
                                    "classified" => TransformationKind::Classified,
                                    "sent_to_llm" => TransformationKind::SentToLlm,
                                    "written_to_output" => TransformationKind::WrittenToOutput,
                                    "delegated_to_agent" => TransformationKind::DelegatedToAgent,
                                    other => TransformationKind::Custom(other.to_string()),
                                };
                                let description = obj
                                    .get("description")
                                    .and_then(|v| v.as_str())
                                    .unwrap_or("")
                                    .to_string();
                                lineage.transformations.push(Transformation {
                                    kind,
                                    agent_id: event.agent_id,
                                    timestamp: event.timestamp,
                                    description,
                                });
                            }
                        }
                    }
                }
                "provenance.delegation" => {
                    if let (Some(data_id_str), Some(to_agent_str)) = (
                        obj.get("data_id").and_then(|v| v.as_str()),
                        obj.get("to_agent").and_then(|v| v.as_str()),
                    ) {
                        if let (Ok(data_id), Ok(to_agent)) =
                            (Uuid::parse_str(data_id_str), Uuid::parse_str(to_agent_str))
                        {
                            if let Some(lineage) = self.lineage.get_mut(&data_id) {
                                let reason = obj
                                    .get("reason")
                                    .and_then(|v| v.as_str())
                                    .unwrap_or("")
                                    .to_string();
                                lineage.transformations.push(Transformation {
                                    kind: TransformationKind::DelegatedToAgent,
                                    agent_id: event.agent_id,
                                    timestamp: event.timestamp,
                                    description: format!(
                                        "Delegated to agent {} — {}",
                                        to_agent, reason
                                    ),
                                });
                                lineage.current_holder = to_agent;
                            }
                        }
                    }
                }
                _ => {}
            }
        }
    }

    /// Number of lineage entries currently tracked.
    pub fn entry_count(&self) -> usize {
        self.lineage.len()
    }
}

fn current_timestamp() -> u64 {
    std::time::SystemTime::now()
        .duration_since(std::time::UNIX_EPOCH)
        .map(|d| d.as_secs())
        .unwrap_or(0)
}

